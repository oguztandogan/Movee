import Foundation
import UIKit

extension UIColor {

    @nonobjc class var borderGray: UIColor {
       return UIColor(red:229/255, green:229/255, blue:234/255, alpha: 1)
     }
    
    @nonobjc class var darkBlue: UIColor {
      return UIColor(red:0/255, green:61/255, blue:255/255, alpha: 1)
    }
    
    @nonobjc class var vibrantBlue: UIColor {
      return UIColor(red: 0.0, green: 61.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var lightGreyBlue30: UIColor {
      return UIColor(red: 171.0 / 255.0, green: 180.0 / 255.0, blue: 189.0 / 255.0, alpha: 0.3)
    }
}

